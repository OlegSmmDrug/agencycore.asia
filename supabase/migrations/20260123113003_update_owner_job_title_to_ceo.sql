/*
  # Обновление должности владельцев на CEO
  
  1. Изменения
    - Обновляет job_title с 'Владелец' на 'CEO' для всех пользователей
    - Это дает пользователям-владельцам доступ к дашборду директора
  
  2. Примечания
    - Изменение влияет только на существующих пользователей с должностью 'Владелец'
    - Новые пользователи будут автоматически получать должность 'CEO' при регистрации
*/

-- Обновляем должность для всех пользователей-владельцев
UPDATE users 
SET job_title = 'CEO'
WHERE job_title = 'Владелец';
